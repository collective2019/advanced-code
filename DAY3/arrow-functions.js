//ES6 arrow functions

//normal ES5 function
function sayHi() {
  console.log('hiiiiiiiii');
}

sayHi();

//store anonymous function in a variable
var sayHello = function() {
  console.log('Hello');
};

sayHello();

//store anonymous arrow function in a variable
var sayHey = () => {
  console.log('sayHey');
};

sayHey();
